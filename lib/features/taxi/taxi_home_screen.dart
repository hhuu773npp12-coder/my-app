}

class _TaxiHomeScreenState extends State<TaxiHomeScreen> {
  bool isActive = true;
  late String driverId;
  double walletBalance = 0.0;

  @override
  void initState() {
    super.initState();
    _loadDriverId();
  }

  Future<void> _loadDriverId() async {
    final prefs = await SharedPreferences.getInstance();
    driverId = prefs.getString('userId') ?? '';
    _loadWalletBalance();
  }

  Future<void> _loadWalletBalance() async {
    if (driverId.isEmpty) return;
    final doc = await FirebaseFirestore.instance
        .collection("taxi_drivers")
        .doc(driverId)
        .get();
    if (!mounted) return;
    if (doc.exists) {
      setState(() {
        walletBalance = (doc.data()?["balance"] ?? 0).toDouble();
        isActive = doc.data()?["active"] ?? true;
      });
    }
  }

  void _toggleActive() {
    setState(() => isActive = !isActive);
    FirebaseFirestore.instance
        .collection("taxi_drivers")
        .doc(driverId)
        .update({"active": isActive});
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: const Text("ŸÖÿ≥Ÿäÿ®ÿßŸàŸä"), centerTitle: true),
      body: Column(
        children: [
          Padding(
            padding: const EdgeInsets.all(8.0),
            child: Row(
              mainAxisAlignment: MainAxisAlignment.spaceEvenly,
              children: [
                IconButton(
                  icon: const Icon(Icons.person, size: 30),
                  onPressed: () {
                    Navigator.push(
                        context,
                        MaterialPageRoute(
                            builder: (_) => ProfilePage(userId: driverId)));
                  },
                ),
                Stack(
                  alignment: Alignment.center,
                  children: [
                    IconButton(
                      icon: const Icon(Icons.account_balance_wallet, size: 30),
                      onPressed: () {
                        Navigator.push(
                            context,
                            MaterialPageRoute(
                                builder: (_) => WalletPage(userId: driverId)));
                      },
                    ),
                    Positioned(
                      right: 0,
                      bottom: 0,
                      child: Text("${walletBalance.toInt()} ÿØ.ÿπ",
                          style: const TextStyle(
                              fontSize: 12, fontWeight: FontWeight.bold)),
                    ),
                  ],
                ),
                IconButton(
                  icon: const Icon(Icons.list_alt, size: 30),
                  onPressed: () {
                    Navigator.push(
                        context,
                        MaterialPageRoute(
                            builder: (_) =>
                                TaxiOrdersScreen(driverId: driverId)));
                  },
                ),
                IconButton(
                  icon: const Icon(Icons.notifications, size: 30),
                  onPressed: () {
                    Navigator.push(
                        context,
                        MaterialPageRoute(
                            builder: (_) =>
                                NotificationsPage(userId: driverId)));
                  },
                ),
              ],
            ),
          ),
          const Divider(),
          Padding(
            padding: const EdgeInsets.all(8.0),
            child: ElevatedButton(
              style: ElevatedButton.styleFrom(
                  backgroundColor: isActive ? Colors.green : Colors.grey),
              onPressed: _toggleActive,
              child: Text(isActive ? "ŸÜÿ¥ÿ∑" : "ÿ∫Ÿäÿ± ŸÜÿ¥ÿ∑"),
            ),
          ),
        ],
      ),
    );
  }
}

